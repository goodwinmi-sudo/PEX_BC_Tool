        service_drive = slides_engine.get_drive_client(creds)
        
        append_event(job_id, {"type": "log", "message": "[INFO] Cloning Target Presentation"})
        
        try:
            copied_file = slides_engine.copy_presentation(service_drive, source_presentation_id, 'Hardened Deal Package (PEX BC V3) - Autogenerated')
            presentation_id = copied_file.get('id')
        except Exception as e:
            append_event(job_id, {"type": "log", "message": f"[CRITICAL] Failed to copy presentation: {e}"})
            raise e
        
        append_event(job_id, {"type": "log", "message": "[INFO] Reading Slide Elements"})
        append_event(job_id, {"type": "log", "message": f"[INFO] Analyzing geometric coordinate mapping for {source_presentation_id}..."})
        slides_data = slides_engine.extract_presentation_data(service_slides, presentation_id)
        append_event(job_id, {"type": "log", "message": f"[SUCCESS] Extracted {len(slides_data)} slides"})
        
        append_event(job_id, {"type": "step", "index": 1, "title": "Track Determination", "progress": 10})
        append_event(job_id, {"type": "log", "message": "[INFO] Identifying Review Track based on metrics"})
        
        active_personas = rule_engine.get_required_personas(deal_desc, org_filter, deal_size_str, risk_flags)
