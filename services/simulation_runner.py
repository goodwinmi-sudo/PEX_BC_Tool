            # STEP 7: Final Payload Assembly
            append_event(job_id, {"type": "step", "index": 7, "title": "Final Deck Assembly", "progress": 95})
            append_event(job_id, {"type": "log", "message": "[INFO] Mapping virtual updated deck to Google Slides API parameters"})
            
            requests_payload = []
            for original_slide, final_slide in zip(slides_data, final_virtual_deck):
                for obj_id in original_slide['text_boxes'].keys():
                    final_text = final_slide['text_boxes'][obj_id]
                    if original_slide['text_boxes'][obj_id] != final_text:
                        requests_payload.extend([
                            {
                                'deleteText': {
                                    'objectId': obj_id,
                                    'textRange': {'type': 'ALL'}
                                }
                            },
                            {
                                'insertText': {
                                    'objectId': obj_id,
                                    'insertionIndex': 0,
                                    'text': final_text
                                }
                            }
                        ])
            
            payload = ai_engine.run_premorteum_node(critiques, workspace_context, str([s['text'] for s in slides_data])) 
            

