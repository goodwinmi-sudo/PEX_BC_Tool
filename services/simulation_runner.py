        append_event(job_id, {"type": "step", "index": 4, "title": "Approver OKR Fetch", "progress": 25})
        append_event(job_id, {"type": "log", "message": "[INFO] Fetching Moma OKRs and Risk Tolerance"})
        append_event(job_id, {"type": "log", "message": "[SUCCESS] Loaded specific liability thresholds"})
        
        if method == 'template':
            # STEP 5: BC Simulation Loop (Original Deck Review)
            append_event(job_id, {"type": "step", "index": 5, "title": "Executing Directed Board Critiques", "progress": 50})
            append_event(job_id, {"type": "log", "message": f"[INFO] Orchestrating sequential Agent Debate ({len(active_personas)} personas) on original layout"})
            append_event(job_id, {"type": "log", "message": "[INFO] Loading Board Executive prompt matrices..."})
            critiques = run_sequential_debate_personas(active_personas, slides_data, deal_desc, research_context, workspace_context, job_id)
            append_event(job_id, {"type": "log", "message": f"[CRITICAL] Alignment gaps found in Terms ({len(critiques.split('###')) - 1} critiques generated)"})
            
